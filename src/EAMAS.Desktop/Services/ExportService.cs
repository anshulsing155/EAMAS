    /// <summary>All data collected for one report run — passed to both PDF and Excel exporters.</summary>
    public class ReportBundle
    {
        public string OrganizationName  { get; set; } = string.Empty;
        public string EmployeeName      { get; set; } = string.Empty;
        public string EmployeeRole      { get; set; } = string.Empty;
        public string EmployeeDepartment{ get; set; } = string.Empty;
        public string GeneratedBy       { get; set; } = string.Empty;
        public DateTime From            { get; set; }
        public DateTime To              { get; set; }   // exclusive upper bound
        public DateTime GeneratedAt     { get; set; } = DateTime.Now;

        // Core daily data
        public List<DailySummary>    DailySummaries  { get; set; } = new();
        // Per-app detail
        public List<AppUsageDetail>  AppDetails      { get; set; } = new();
        // Category totals
        public Dictionary<ActivityCategory, TimeSpan> CategoryTotals { get; set; } = new();
        // Hour 0-23 activity
        public List<HourlyActivity>  HourlyBreakdown { get; set; } = new();
        // Alerts fired in period
        public List<Alert>           Alerts          { get; set; } = new();
        // Audit log (admin only; empty otherwise)
        public List<AuditLog>        AuditLogs       { get; set; } = new();

        // Derived totals (computed once in ViewModel)
        public TimeSpan TotalActive      { get; set; }
        public TimeSpan TotalIdle        { get; set; }
        public TimeSpan TotalProductive  { get; set; }
        public TimeSpan TotalDistracting { get; set; }
        public int      AvgScore         { get; set; }
        public int      TotalScreenshots { get; set; }

        public string PeriodLabel =>
            $"{From:MMM d, yyyy}  –  {To.AddDays(-1):MMM d, yyyy}";
    }

    public static class ExportService
    {
        static ExportService()
        {
            QuestPDF.Settings.License = LicenseType.Community;
        }

        // ── Excel ────────────────────────────────────────────────────────────────

        public static void ExportToExcel(ReportBundle r, string path)
        {
            using var wb = new XLWorkbook();

            AddSummarySheet(wb, r);
            AddDailySheet(wb, r);
            AddAppUsageSheet(wb, r);
            AddCategorySheet(wb, r);
            AddHourlySheet(wb, r);
            if (r.Alerts.Any())   AddAlertsSheet(wb, r);
            if (r.AuditLogs.Any()) AddAuditSheet(wb, r);

            wb.SaveAs(path);
        }

        private static void AddSummarySheet(XLWorkbook wb, ReportBundle r)
        {
            var ws = wb.Worksheets.Add("Summary");
            ws.ShowGridLines = false;

            // Title block
            ws.Cell(1, 1).Value = "EAMAS — Employee Activity Report";
            ws.Cell(1, 1).Style.Font.Bold = true;
            ws.Cell(1, 1).Style.Font.FontSize = 16;
            ws.Cell(1, 1).Style.Font.FontColor = XLColor.FromHtml("#1E40AF");
            ws.Range(1, 1, 1, 6).Merge();

            ws.Cell(2, 1).Value = r.PeriodLabel;
            ws.Cell(2, 1).Style.Font.FontSize = 11;
            ws.Cell(2, 1).Style.Font.FontColor = XLColor.FromHtml("#64748B");
            ws.Range(2, 1, 2, 6).Merge();

            int row = 4;
            void Info(string label, string value)
            {
                ws.Cell(row, 1).Value = label;
                ws.Cell(row, 1).Style.Font.Bold = true;
                ws.Cell(row, 1).Style.Font.FontColor = XLColor.FromHtml("#64748B");
                ws.Cell(row, 2).Value = value;
                row++;
            }

            Info("Organisation", r.OrganizationName);
            Info("Employee",     r.EmployeeName);
            Info("Role",         r.EmployeeRole);
            Info("Department",   r.EmployeeDepartment);
            Info("Generated",    r.GeneratedAt.ToString("yyyy-MM-dd HH:mm"));
            Info("Generated By", r.GeneratedBy);
            row++;

            // KPI table
            var kpiHeaders = new[] { "METRIC", "VALUE" };
            WriteHeaderRow(ws, row, kpiHeaders, "#1E40AF");
            row++;

            void Kpi(string metric, string value)
            {
                ws.Cell(row, 1).Value = metric;
                ws.Cell(row, 2).Value = value;
                AlternateRow(ws, row, 2);
                row++;
            }

            Kpi("Total Active Time",        FormatTime(r.TotalActive));
            Kpi("Total Idle Time",          FormatTime(r.TotalIdle));
            Kpi("Total Productive Time",    FormatTime(r.TotalProductive));
            Kpi("Total Distracting Time",   FormatTime(r.TotalDistracting));
            Kpi("Average Productivity Score", $"{r.AvgScore}%");
            Kpi("Total Screenshots",        r.TotalScreenshots.ToString());
            Kpi("Active Days",              r.DailySummaries.Count(s => s.ActiveTime.TotalMinutes >= 5).ToString());
            Kpi("Best Day Score",           r.DailySummaries.Any()
                ? $"{r.DailySummaries.Max(s => s.ProductivityScore)}% — {r.DailySummaries.OrderByDescending(s => s.ProductivityScore).First().Date:ddd MMM d}"
                : "—");
            Kpi("Alert Count",              r.Alerts.Count.ToString());

            ws.Column(1).Width = 28;
            ws.Column(2).Width = 40;
        }

        private static void AddDailySheet(XLWorkbook wb, ReportBundle r)
        {
            var ws = wb.Worksheets.Add("Daily Breakdown");
            ws.ShowGridLines = false;

            var headers = new[]
            {
                "DATE", "DAY", "ACTIVE TIME", "IDLE TIME",
                "PRODUCTIVE", "DISTRACTING", "NEUTRAL",
                "SCORE", "SCREENSHOTS", "TOP APP"
            };
            WriteHeaderRow(ws, 1, headers, "#1E40AF");

            int row = 2;
            foreach (var s in r.DailySummaries)
            {
                var neutral = s.ActiveTime - s.ProductiveTime - s.DistractingTime;
                if (neutral < TimeSpan.Zero) neutral = TimeSpan.Zero;
                ws.Cell(row, 1).Value  = s.Date.ToString("yyyy-MM-dd");
                ws.Cell(row, 2).Value  = s.Date.ToString("dddd");
                ws.Cell(row, 3).Value  = FormatTime(s.ActiveTime);
                ws.Cell(row, 4).Value  = FormatTime(s.IdleTime);
                ws.Cell(row, 5).Value  = FormatTime(s.ProductiveTime);
                ws.Cell(row, 6).Value  = FormatTime(s.DistractingTime);
                ws.Cell(row, 7).Value  = FormatTime(neutral);
                ws.Cell(row, 8).Value  = s.ProductivityScore;
                ws.Cell(row, 9).Value  = s.ScreenshotCount;
                ws.Cell(row, 10).Value = s.TopApps.FirstOrDefault().App ?? "—";

                // Score cell color
                var scoreCell = ws.Cell(row, 8);
                scoreCell.Style.Font.Bold = true;
                scoreCell.Style.Font.FontColor = s.ProductivityScore >= 70
                    ? XLColor.FromHtml("#16A34A")
                    : s.ProductivityScore >= 40
                        ? XLColor.FromHtml("#D97706")
                        : XLColor.FromHtml("#DC2626");

                AlternateRow(ws, row, 10);
                row++;
            }

            AutofitColumns(ws, 10);
        }

        private static void AddAppUsageSheet(XLWorkbook wb, ReportBundle r)
        {
            var ws = wb.Worksheets.Add("App Usage");
            ws.ShowGridLines = false;

            var headers = new[]
            {
                "APPLICATION", "PROCESS", "CATEGORY",
                "TOTAL TIME", "% OF ACTIVE", "PRODUCTIVITY RATING"
            };
            WriteHeaderRow(ws, 1, headers, "#1E40AF");

            var totalActiveTicks = r.TotalActive.Ticks < 1 ? 1 : r.TotalActive.Ticks;
            int row = 2;
            foreach (var a in r.AppDetails)
            {
                var pct = (double)a.TotalDuration.Ticks / totalActiveTicks * 100;
                ws.Cell(row, 1).Value = a.ApplicationName;
                ws.Cell(row, 2).Value = a.ProcessName;
                ws.Cell(row, 3).Value = a.Category.ToString();
                ws.Cell(row, 4).Value = FormatTime(a.TotalDuration);
                ws.Cell(row, 5).Value = $"{pct:F1}%";
                ws.Cell(row, 6).Value = $"{a.ProductivityRating}%";

                // Category colour
                ws.Cell(row, 3).Style.Font.FontColor = a.Category switch
                {
                    ActivityCategory.Productive   => XLColor.FromHtml("#16A34A"),
                    ActivityCategory.Distracting  => XLColor.FromHtml("#DC2626"),
                    ActivityCategory.Neutral      => XLColor.FromHtml("#D97706"),
                    _                             => XLColor.FromHtml("#64748B")
                };

                AlternateRow(ws, row, 6);
                row++;
            }

            AutofitColumns(ws, 6);
        }

        private static void AddCategorySheet(XLWorkbook wb, ReportBundle r)
        {
            var ws = wb.Worksheets.Add("Category Analysis");
            ws.ShowGridLines = false;

            ws.Cell(1, 1).Value = "TIME BY CATEGORY";
            ws.Cell(1, 1).Style.Font.Bold = true;
            ws.Cell(1, 1).Style.Font.FontSize = 13;

            var headers = new[] { "CATEGORY", "TOTAL TIME", "PERCENTAGE", "DESCRIPTION" };
            WriteHeaderRow(ws, 2, headers, "#1E40AF");

            var totalTicks = r.CategoryTotals.Values.Sum(t => t.Ticks);
            if (totalTicks == 0) totalTicks = 1;

            int row = 3;
            var catDescriptions = new Dictionary<ActivityCategory, string>
            {
                [ActivityCategory.Productive]  = "Work-related applications contributing positively",
                [ActivityCategory.Neutral]     = "General or uncategorised applications",
                [ActivityCategory.Distracting] = "Non-work applications reducing productivity",
                [ActivityCategory.Unknown]     = "Newly seen applications not yet categorised"
            };

            foreach (ActivityCategory cat in Enum.GetValues<ActivityCategory>())
            {
                r.CategoryTotals.TryGetValue(cat, out var ts);
                var pct = (double)ts.Ticks / totalTicks * 100;
                ws.Cell(row, 1).Value = cat.ToString();
                ws.Cell(row, 2).Value = FormatTime(ts);
                ws.Cell(row, 3).Value = $"{pct:F1}%";
                ws.Cell(row, 4).Value = catDescriptions.GetValueOrDefault(cat, "");
                AlternateRow(ws, row, 4);
                row++;
            }

            // Also add idle
            ws.Cell(row, 1).Value = "Idle";
            ws.Cell(row, 2).Value = FormatTime(r.TotalIdle);
            ws.Cell(row, 3).Value = "—";
            ws.Cell(row, 4).Value = "No keyboard/mouse input detected";
            AlternateRow(ws, row, 4);

            AutofitColumns(ws, 4);
        }

        private static void AddHourlySheet(XLWorkbook wb, ReportBundle r)
        {
            var ws = wb.Worksheets.Add("Hourly Patterns");
            ws.ShowGridLines = false;

            var headers = new[]
            {
                "HOUR", "TIME SLOT", "ACTIVE TIME",
                "PRODUCTIVE TIME", "DISTRACTING TIME", "PRODUCTIVITY %"
            };
            WriteHeaderRow(ws, 1, headers, "#1E40AF");

            int row = 2;
            foreach (var h in r.HourlyBreakdown)
            {
                var prodPct = h.ActiveTime.Ticks < 1 ? 0
                    : (double)h.ProductiveTime.Ticks / h.ActiveTime.Ticks * 100;

                ws.Cell(row, 1).Value = h.Hour;
                ws.Cell(row, 2).Value = $"{h.Hour:D2}:00 – {(h.Hour + 1) % 24:D2}:00";
                ws.Cell(row, 3).Value = FormatTime(h.ActiveTime);
                ws.Cell(row, 4).Value = FormatTime(h.ProductiveTime);
                ws.Cell(row, 5).Value = FormatTime(h.DistractingTime);
                ws.Cell(row, 6).Value = $"{prodPct:F0}%";

                if (h.ActiveTime.TotalMinutes >= 30)
                    ws.Cell(row, 3).Style.Font.Bold = true;

                AlternateRow(ws, row, 6);
                row++;
            }

            AutofitColumns(ws, 6);
        }

        private static void AddAlertsSheet(XLWorkbook wb, ReportBundle r)
        {
            var ws = wb.Worksheets.Add("Alerts");
            ws.ShowGridLines = false;

            var headers = new[] { "DATE & TIME", "TYPE", "MESSAGE", "RESOLVED" };
            WriteHeaderRow(ws, 1, headers, "#991B1B");

            int row = 2;
            foreach (var a in r.Alerts)
            {
