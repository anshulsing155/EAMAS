                ws.Cell(row, 2).Value = a.Type.ToString();
                ws.Cell(row, 3).Value = a.Message;
                ws.Cell(row, 4).Value = a.IsResolved ? "Yes" : "No";

                ws.Cell(row, 4).Style.Font.FontColor = a.IsResolved
                    ? XLColor.FromHtml("#16A34A")
                    : XLColor.FromHtml("#DC2626");

                AlternateRow(ws, row, 4);
                row++;
            }

            AutofitColumns(ws, 4);
        }

