        // ── PDF ──────────────────────────────────────────────────────────────────

        public static void ExportToPdf(ReportBundle r, string path)
        {
            Document.Create(container =>
            {
                container.Page(page =>
                {
                    page.Size(PageSizes.A4);
                    page.Margin(2, Unit.Centimetre);
                    page.DefaultTextStyle(t => t.FontFamily("Arial").FontSize(9).FontColor("#1E293B"));

                    page.Header().Element(c => PdfHeader(c, r));
                    page.Footer().Element(c => PdfFooter(c, r));
                    page.Content().Element(c => PdfContent(c, r));
                });
            }).GeneratePdf(path);
        }

        private static void PdfHeader(IContainer c, ReportBundle r)
        {
            c.BorderBottom(1).BorderColor("#E2E8F0").PaddingBottom(10).Row(row =>
            {
                row.RelativeItem().Column(col =>
                {
                    col.Item().Text("EAMAS — Employee Activity Report")
                        .FontSize(16).Bold().FontColor("#1E40AF");
                    col.Item().Text(r.PeriodLabel)
                        .FontSize(9).FontColor("#64748B");
                });
                row.ConstantItem(160).AlignRight().Column(col =>
                {
                    col.Item().Text(r.OrganizationName).Bold().AlignRight();
                    col.Item().Text($"Generated: {r.GeneratedAt:yyyy-MM-dd HH:mm}").FontColor("#64748B").AlignRight();
                });
            });
        }

        private static void PdfFooter(IContainer c, ReportBundle r)
        {
            c.BorderTop(1).BorderColor("#E2E8F0").PaddingTop(6).Row(row =>
            {
                row.RelativeItem().Text($"Confidential — {r.OrganizationName}").FontColor("#94A3B8").FontSize(8);
                row.RelativeItem().AlignCenter().Text(txt =>
                {
                    txt.Span("Page ").FontColor("#94A3B8").FontSize(8);
                    txt.CurrentPageNumber().FontColor("#94A3B8").FontSize(8);
                    txt.Span(" of ").FontColor("#94A3B8").FontSize(8);
                    txt.TotalPages().FontColor("#94A3B8").FontSize(8);
                });
                row.RelativeItem().AlignRight().Text("EAMAS v" + UpdateService.CurrentVersion.ToString(3)).FontColor("#94A3B8").FontSize(8);
            });
        }

        private static void PdfContent(IContainer c, ReportBundle r)
        {
            c.Column(col =>
            {
                col.Spacing(16);

                // ── Employee info block
                col.Item().Element(x => PdfInfoBlock(x, r));

                // ── KPI summary
                col.Item().Element(x => PdfKpiTable(x, r));

                // ── Daily breakdown
                col.Item().Element(x => PdfDailyTable(x, r));

                // ── App usage
                if (r.AppDetails.Any())
                    col.Item().Element(x => PdfAppTable(x, r));

                // ── Category analysis
                if (r.CategoryTotals.Any())
                    col.Item().Element(x => PdfCategoryTable(x, r));

                // ── Hourly patterns
                col.Item().Element(x => PdfHourlyTable(x, r));

                // ── Alerts
                if (r.Alerts.Any())
                    col.Item().Element(x => PdfAlertsTable(x, r));

                // ── Audit log
                if (r.AuditLogs.Any())
                    col.Item().Element(x => PdfAuditTable(x, r));
            });
        }

