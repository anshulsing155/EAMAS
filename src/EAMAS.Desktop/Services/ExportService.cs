                    foreach (ActivityCategory cat in Enum.GetValues<ActivityCategory>())
                    {
                        r.CategoryTotals.TryGetValue(cat, out var ts);
                        var bg = alt ? "#F8FAFC" : "#FFFFFF";
                        alt = !alt;
                        PdfCell(t, cat.ToString(), bg);
                        PdfCell(t, FormatTime(ts), bg);
                        PdfCell(t, $"{(double)ts.Ticks / totalTicks * 100:F1}%", bg, center: true);
                        PdfCell(t, descriptions.GetValueOrDefault(cat, ""), bg);
                    }

                    // Idle row
                    var idleBg = alt ? "#F8FAFC" : "#FFFFFF";
                    PdfCell(t, "Idle", idleBg);
                    PdfCell(t, FormatTime(r.TotalIdle), idleBg);
                    PdfCell(t, "—", idleBg, center: true);
                    PdfCell(t, "No keyboard/mouse input detected", idleBg);
                });
            });
        }

        private static void PdfHourlyTable(IContainer c, ReportBundle r)
        {
            var activeHours = r.HourlyBreakdown.Where(h => h.ActiveTime.TotalMinutes >= 1).ToList();
            if (!activeHours.Any()) return;

            c.Column(col =>
            {
                col.Item().Text("Hourly Activity Patterns").FontSize(12).Bold().FontColor("#1E40AF");
                col.Item().PaddingTop(6).Table(t =>
                {
                    t.ColumnsDefinition(cd =>
                    {
                        cd.ConstantColumn(70);
                        cd.RelativeColumn(2);
                        cd.RelativeColumn(2);
                        cd.RelativeColumn(2);
                        cd.RelativeColumn(1.5f);
                    });

                    PdfTableHeader(t, new[] { "HOUR", "TIME SLOT", "ACTIVE", "PRODUCTIVE", "PROD %" });

                    bool alt = false;
                    foreach (var h in activeHours)
                    {
                        var bg = alt ? "#F8FAFC" : "#FFFFFF";
                        alt = !alt;
                        var prodPct = h.ActiveTime.Ticks < 1 ? 0
                            : (double)h.ProductiveTime.Ticks / h.ActiveTime.Ticks * 100;

                        PdfCell(t, $"{h.Hour:D2}:00", bg, center: true);
                        PdfCell(t, $"{h.Hour:D2}:00 – {(h.Hour + 1) % 24:D2}:00", bg);
                        PdfCell(t, FormatTime(h.ActiveTime), bg);
                        PdfCell(t, FormatTime(h.ProductiveTime), bg);
                        PdfCell(t, $"{prodPct:F0}%", bg, center: true);
                    }
                });
            });
        }

