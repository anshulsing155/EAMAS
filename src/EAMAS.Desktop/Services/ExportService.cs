        // ── PDF helpers ──────────────────────────────────────────────────────────

        private static void PdfTableHeader(TableDescriptor t, string[] headers,
            string bgColor = "#1E40AF")
        {
            foreach (var h in headers)
                t.Cell().Background(bgColor).Padding(6)
                    .Text(h).FontColor("#FFFFFF").Bold().FontSize(8);
        }

        private static void PdfCell(TableDescriptor t, string text, string bg,
            bool bold = false, bool center = false)
        {
            // AlignCenter() must be chained BEFORE Text() — QuestPDF containers are
            // single-child; calling AlignCenter() after Text() assigns a second child.
            var cell = t.Cell().Background(bg).Padding(5);
            var leaf = center ? cell.AlignCenter() : cell;
            var txt  = leaf.Text(text).FontSize(8);
            if (bold) txt.Bold();
        }

        // ── Excel helpers ────────────────────────────────────────────────────────

        private static void WriteHeaderRow(IXLWorksheet ws, int row, string[] headers, string hexColor)
        {
            for (int i = 0; i < headers.Length; i++)
            {
                var cell = ws.Cell(row, i + 1);
                cell.Value = headers[i];
                cell.Style.Font.Bold = true;
                cell.Style.Font.FontColor = XLColor.White;
                cell.Style.Fill.BackgroundColor = XLColor.FromHtml(hexColor);
                cell.Style.Alignment.Horizontal = XLAlignmentHorizontalValues.Left;
            }
        }

        private static void AlternateRow(IXLWorksheet ws, int row, int colCount)
        {
            if (row % 2 == 0)
                ws.Range(row, 1, row, colCount).Style.Fill.BackgroundColor = XLColor.FromHtml("#F8FAFC");
        }

        private static void AutofitColumns(IXLWorksheet ws, int colCount)
        {
            for (int i = 1; i <= colCount; i++)
                ws.Column(i).AdjustToContents();
        }

        // ── Shared ───────────────────────────────────────────────────────────────

        public static string FormatTime(TimeSpan ts)
        {
            if (ts.TotalHours >= 1) return $"{(int)ts.TotalHours}h {ts.Minutes:D2}m";
            if (ts.TotalMinutes >= 1) return $"{ts.Minutes}m";
            return ts.TotalSeconds < 1 ? "—" : $"{ts.Seconds}s";
        }
