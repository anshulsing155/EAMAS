        private static void PdfCategoryTable(IContainer c, ReportBundle r)
        {
            c.Column(col =>
            {
                col.Item().Text("Category Analysis").FontSize(12).Bold().FontColor("#1E40AF");
                col.Item().PaddingTop(6).Table(t =>
                {
                    t.ColumnsDefinition(cd =>
                    {
                        cd.RelativeColumn(2);
                        cd.RelativeColumn(2);
                        cd.RelativeColumn(1.5f);
                        cd.RelativeColumn(4);
                    });

                    PdfTableHeader(t, new[] { "CATEGORY", "TOTAL TIME", "PERCENTAGE", "DESCRIPTION" });

                    var totalTicks = r.CategoryTotals.Values.Sum(x => x.Ticks);
                    if (totalTicks == 0) totalTicks = 1;
                    bool alt = false;

                    var descriptions = new Dictionary<ActivityCategory, string>
                    {
                        [ActivityCategory.Productive]  = "Work-related, score-boosting activity",
                        [ActivityCategory.Neutral]      = "General or uncategorised usage",
                        [ActivityCategory.Distracting]  = "Non-work applications reducing score",
                        [ActivityCategory.Unknown]       = "Not yet categorised by the system"
