        {
            c.Column(col =>
            {
                col.Item().Text("Application Usage").FontSize(12).Bold().FontColor("#1E40AF");
                col.Item().PaddingTop(6).Table(t =>
                {
                    t.ColumnsDefinition(cd =>
                    {
                        cd.RelativeColumn(3);
